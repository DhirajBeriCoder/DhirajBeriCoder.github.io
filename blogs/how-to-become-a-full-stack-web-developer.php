			hardware and operating systems.
		</li>
		<li>
			Create and design front end by using HTML, CSS, and JavaScript.
		</li>
		<li>
			Build interactive UIs with the help of JavaScript frameworks, such
			as ReactJS, AngularJS, VueJs, Ember, etc.
		</li>
		<li>
			Code server-side APIs and back end by using programming languages
			like Python, Ruby, Java, PHP, and frameworks like NodeJS.
		</li>
		<li>
			Manage and operate Relational Database Management Systems like
			MySQL, MongoDB, SQL, etc.
		</li>
		<li>
			Must be well versed with Project Management tools and client
			co-ordination.
