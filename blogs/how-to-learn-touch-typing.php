    Space bar.
</p>
<h3>
    Fingers motion
</h3>
<p>
    Don't look at the keys when you type. Just slide your fingers around until
    they find the home row marking.
</p>
<p>
    Limit your hand and finger movement only to what is necessary to press a
    specific key. Keep your hands and fingers close to the base position. This
    improves typing speed and reduces stress on the hands.
</p>
<p>
    Pay attention to ring fingers and little fingers, since they are
    considerably underdeveloped.
</p>
